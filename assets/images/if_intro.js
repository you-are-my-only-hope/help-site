

// -----------------  If Statement -------------------- 

// Block Level Code
// HAS to take a parameter
// Does not need a else statement unless needed

// Start with key work 'if'
// Then () this is where the parameter is entered
// Then curly braces 

if (5 == 5) {

	// If the condition is met or true in the ()
	// will do the logic inside the curly brace

	console.log("They are equal");

} // End of If Statement

// -----------------  If Else Statement -------------------- 

// Block Level Code
// Else does NOT take a parameter
// Has to have an if statement attached to it

// Start with an if statement like above 
// After the curly brace for the if statement
// type the key word else


if (5 == 6) {

	// If the condition is met or true in the ()
	// will do the logic inside the curly braces

	console.log("They are equal");

} else {

	// If the condition is NOT met in the () 
	// for the if statement will NOT run

	console.log("They are NOT equal");

} // End of If Else Statement


// -----------------  Else If Statement --------------------

// Block Level Code
// HAS to take a parameter
// Does not need a else statement unless needed

// This statement is possibly need if multiple 
// condition need to be tested

// Start with an If statement like the above examples 
// After the curly brace for the if statement 
// type the key word else if
// There can be as many else if statement that are possibly needed
// The possible else statement goes after the else if statements

if (5 == 6) {

	// If the condition is met or true in the ()
	// will do the logic inside the curly braces

	console.log("They are equal");

} else if (5 == 5) {

	console.log("They are equal");

} else {

	console.log("They are not equal");		

} // End of If Else If Else Statement











